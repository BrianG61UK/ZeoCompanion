    // internal handler to move state change detections from the Runnable thread into the main thread
    private Handler mEmailTestHandler = new Handler() {
        @Override
        public void handleMessage(Message msg) {
            if (msg.what == ZeoCompanionApplication.MESSAGE_SETTINGS_EMAILTEST_RESULTS) {
                String header = "Successful Test Results";
                if (msg.arg1 == 0) { header = "Failed Test Results"; }
                Utilities.showAlertDialog(SettingsActivity.this, header, (String)msg.obj, "Okay");
            }
        }
    };

    // thread context:  DirectEmailerThread
    DirectEmailerThread.DirectEmailerThreadResponse mEmailResult = new DirectEmailerThread.DirectEmailerThreadResponse() {
        @Override
        public void emailResults(String ignored, boolean successFail, String message) {
            Message msg = new Message();
            msg.what = ZeoCompanionApplication.MESSAGE_SETTINGS_EMAILTEST_RESULTS;
            if (successFail) { msg.arg1 = 1; }
            else { msg.arg1 = 0; }
            msg.obj = message;
            mEmailTestHandler.sendMessage(msg);
        }
    };

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setupActionBar();

        // force the breadcrumbs area to conform to my theme
        View tv = findViewById(android.R.id.title);
        if (tv != null) {
            View lla = (View)tv.getParent();
            if (lla != null) {
                lla.setBackgroundColor(getResources().getColor(R.color.colorOffBlack3));
                View llb = (View) lla.getParent();
                if (llb != null) {
                    llb.setBackgroundColor(getResources().getColor(R.color.colorOffBlack2));
                }
            }
        }
    }

